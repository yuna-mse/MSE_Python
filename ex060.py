nums = [1, 2, 3, 4, 5]

a = sum(nums)                  
#먼저 sum 함수를 이용해 리스트 원소의 총 합을 구하고 변수 a에 대입한다. (= 15)
b = len(nums)                  
#다음으로 len 함수를 이용해 리스트 원소의 갯수를 구하고 변수 b에 대입한다 ( = 5)
average = a/b                  
'''리스트 원소의 총 합을 리스트 원소의 개수로 나누면 리스트의 평균을 구할 수 있다. (= 3.0)
   이 값을 변수 average에 대입한다.'''
print(average)
